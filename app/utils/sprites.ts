export function parseSpriteData(spriteString: string): number[][] {
  const lines = spriteString.trim().split('\n').map(line => line.trim());
  const pixels: number[][] = [];

  for (const line of lines) {
    const row: number[] = [];
    for (const char of line) {
      row.push(char === '.' ? 0 : 1);
    }
    pixels.push(row);
  }

  return pixels;
}
